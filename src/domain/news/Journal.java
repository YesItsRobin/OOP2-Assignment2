package domain.news;

public class Journal {
	private String name;
	
	public Journal(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdentitity() {
		return name;
	}
}
